function showQA1(id) {

    var question_num = document.getElementById(id);
        if ($(question_num).css('display') === 'none') {
            $(question_num).css('display', 'block');
            $(".video_one").css('display', 'block')
        } else {
            $(question_num).css('display', 'none');
            $(".video_one").css('display', 'none');
        };
}
